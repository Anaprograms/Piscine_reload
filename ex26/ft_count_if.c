int	ft_count_if(char **tab, int (*f)(char*))
{
	int	x;
	int	num;

	x = 0;
	num = 0;
	while (tab[x])
	{
		if (f(tab[x]) == 1)
			num++;
		x++;
	}
	return (num);
}
